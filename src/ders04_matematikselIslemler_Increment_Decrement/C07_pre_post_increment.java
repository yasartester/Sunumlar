package ders04_matematikselIslemler_Increment_Decrement;

public class C07_pre_post_increment {
    public static void main(String[] args) {

        int a=10;      // a= 10
        int b= a++;     // b= 10     a=11
        int c= ++b;    // b=11       c=11
        System.out.println("a : "+a+ ",  b : "+b+ ",  c : "+ c);


    }
}
