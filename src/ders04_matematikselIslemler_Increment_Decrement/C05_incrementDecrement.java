package ders04_matematikselIslemler_Increment_Decrement;

public class C05_incrementDecrement {
    public static void main(String[] args) {

        int sayi=10;
           // bu sayiyi 3 artirip yazdiralim
        sayi +=3;  // // 13

        sayi= sayi+3;
        System.out.println("sayi: "+ sayi); // sayi=16

        int s = 10;  // bu sayiyi 1 artirin
        s+=1;  // 11
        s=s+1;  // 12
        s++;    // 13
        ++s;    // 14
        System.out.println("s : "+ s); // s=14

        int t=10;  // bu sayinin degerini bir eksiltelim
        t-=1;      // 9
        t=t-1;     // 8
        t--;       // 7
        --t;       //  t= 6
        System.out.println("t : "+ t); // t= 6












    }
}
