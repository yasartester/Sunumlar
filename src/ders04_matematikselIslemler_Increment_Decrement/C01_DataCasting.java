package ders04_matematikselIslemler_Increment_Decrement;

public class C01_DataCasting {
    public static void main(String[] args) {
        int sayi1= 20;
        int sayi2= 5;
        System.out.println(sayi1/sayi2);  // bu sekilde sonuc 4 olur

        int sayi5= 20;
        int sayi6= 6;
        System.out.println(sayi5/sayi6); // sonuc  3  olur
        // bolunen ve bolenin her ikisi de tam sayi ise Java sonucun sadece tamsayi kismini alir.

        double sayi7=6;
        System.out.println(sayi1/sayi7); // int/double    java genis olana (double) gore davranir  3.3333333333335






    }
}
